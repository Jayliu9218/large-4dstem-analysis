def _render_qc_markdown(result: QCResult) -> str:
    status = result.stage1_status
    emoji = {"PASS": "✅", "PASS_WITH_WARNINGS": "⚠️", "FAIL": "❌"}.get(status, "❓")
    lines = [
        f"# Stage 1 QC Summary",
        "",
        f"**Status: {emoji} {status}**",
        "",
        f"- Warnings: {result.n_warnings}",
        f"- Critical: {result.n_critical}",
        "",
    ]
    if result.flags:
        lines.append("| Severity | Code | Message |")
        lines.append("| --- | --- | --- |")
        for flag in result.flags:
            sev = flag.severity.upper()
            lines.append(f"| `{sev}` | `{flag.code}` | {flag.message} |")
        lines.append("")
    else:
        lines.extend(["_No QC flags._", ""])

    # Interpretation guidance
    lines.extend(
        [
            "## Interpretation Caution",
            "",
            "> The fingerprint-class labels are generated by unsupervised clustering "
            "of radial fingerprints. They should **not** be interpreted as confirmed "
            "crystallographic phases without Stage-2 validation (py4DSTEM / pyxem "
            "Bragg indexing or template matching against candidate CIFs).",
            "",
        ]
    )
    return "\n".join(lines)


def _jsonable(value: Any) -> Any:
    if isinstance(value, dict):
        return {str(k): _jsonable(v) for k, v in value.items()}
    if isinstance(value, (list, tuple)):
        return [_jsonable(v) for v in value]
    if isinstance(value, (np.integer,)):
        return int(value)
    if isinstance(value, (np.floating,)):
        return float(value)
    if isinstance(value, np.ndarray):
        return value.tolist()
    return value
