# Individual checks
# ---------------------------------------------------------------------------


def _check_input_shape(dataset: DatasetHandle | None, flags: list[QCFlag]) -> None:
    if dataset is None:
        flags.append(
            QCFlag(
                severity="critical",
                code="DATASET_MISSING",
                message="Dataset failed to load. No data available for analysis.",
            )
        )
        return
    shape = dataset.shape
    if len(shape) != 4:
        flags.append(
            QCFlag(
                severity="critical",
                code="INPUT_NOT_4D",
                message=f"Expected 4D-STEM data, got shape {shape}.",
                evidence={"shape": list(shape)},
            )
        )
        return
    nav_y, nav_x, q_y, q_x = shape
    if nav_y < 2 or nav_x < 2:
        flags.append(
            QCFlag(
                severity="critical",
                code="NAVIGATION_TOO_SMALL",
                message=f"Navigation dimensions ({nav_y}, {nav_x}) are too small for meaningful analysis.",
                evidence={"nav_shape": [nav_y, nav_x]},
            )
        )
    if q_y < 4 or q_x < 4:
        flags.append(
            QCFlag(
                severity="warning",
                code="SIGNAL_TOO_SMALL",
                message=f"Detector dimensions ({q_y}, {q_x}) are very small; radial fingerprints may be under-resolved.",
                evidence={"sig_shape": [q_y, q_x]},
            )
        )


def _check_virtual_images(virtual: VirtualImageResult | None, flags: list[QCFlag]) -> None:
    if virtual is None:
        flags.append(
            QCFlag(
                severity="critical",
                code="VIRTUAL_IMAGES_MISSING",
                message="Virtual images were not computed. Downstream results are unavailable.",
            )
        )
        return
    if not virtual.images:
        flags.append(
            QCFlag(
                severity="warning",
                code="VIRTUAL_IMAGES_EMPTY",
                message="Virtual images dict is empty; no virtual detector images were produced.",
            )
        )


def _check_radial_fingerprints(fingerprints: FingerprintResult | None, flags: list[QCFlag]) -> None:
    if fingerprints is None:
        flags.append(
            QCFlag(
                severity="critical",
                code="RADIAL_FINGERPRINTS_MISSING",
                message="Radial fingerprints were not computed.",
            )
        )
        return
    profiles = fingerprints.profiles
    if not np.all(np.isfinite(profiles)):
        n_bad = int(np.sum(~np.isfinite(profiles)))
        flags.append(
            QCFlag(
                severity="warning",
                code="RADIAL_PROFILES_NONFINITE",
                message=f"{n_bad} radial-profile values are non-finite (NaN / Inf).",
                evidence={"non_finite_count": n_bad, "total_values": int(profiles.size)},
            )
        )
    # Check for all-zero profiles (degenerate)
    flat = profiles.reshape(-1, profiles.shape[-1])
    zero_rows = int(np.sum(np.all(flat == 0, axis=1)))
    if zero_rows > 0:
        flags.append(
            QCFlag(
                severity="warning",
                code="RADIAL_PROFILES_ZERO_ROWS",
                message=f"{zero_rows} navigation positions have all-zero radial profiles.",
                evidence={"zero_row_count": zero_rows, "total_rows": flat.shape[0]},
            )
        )


def _check_cluster_labels(phase: PhaseScreeningResult | None, flags: list[QCFlag]) -> None:
    if phase is None:
        flags.append(
            QCFlag(
                severity="critical",
                code="CLUSTER_LABELS_MISSING",
                message="Fingerprint-class labels were not produced.",
            )
        )
        return
    labels = np.asarray(phase.labels)
    unique = np.unique(labels)
    if len(unique) < 2:
        flags.append(
            QCFlag(
                severity="warning",
                code="CLUSTER_SINGLE_CLASS",
                message=f"Only {len(unique)} fingerprint class found; clustering may have failed or the sample is uniform.",
                evidence={"n_classes": int(len(unique))},
            )
        )


def _check_cluster_sizes(phase: PhaseScreeningResult | None, flags: list[QCFlag]) -> None:
    if phase is None:
        return
    labels = np.asarray(phase.labels)
    unique, counts = np.unique(labels, return_counts=True)
    total = max(int(counts.sum()), 1)
    min_frac = float(counts.min() / total) if len(counts) > 0 else 0.0

    if min_frac < 0.01:
        small_cluster = int(unique[int(np.argmin(counts))])
        flags.append(
            QCFlag(
                severity="warning",
                code="CLUSTER_TOO_SMALL",
                message=(
                    f"Cluster {small_cluster} occupies only {min_frac:.3%} of the field of view. "
                    "It may be noise rather than a distinct fingerprint class."
                ),
                evidence={
                    "min_cluster_fraction": round(min_frac, 5),
                    "smallest_cluster_id": small_cluster,
                    "cluster_counts": {int(k): int(v) for k, v in zip(unique, counts)},
                },
            )
        )


def _check_beam_center(virtual: VirtualImageResult | None, flags: list[QCFlag]) -> None:
    if virtual is None:
        return
    try:
        mean_dp = np.asarray(virtual.mean_diffraction, dtype=np.float64)
        yy, xx = np.indices(mean_dp.shape)
        total = max(float(mean_dp.sum()), 1e-12)
        cy = float((mean_dp * yy).sum() / total)
        cx = float((mean_dp * xx).sum() / total)
        rc_y = (mean_dp.shape[0] - 1) / 2
        rc_x = (mean_dp.shape[1] - 1) / 2
        offset = float(np.hypot(cy - rc_y, cx - rc_x))

        if offset > 3:
            flags.append(
                QCFlag(
                    severity="warning",
                    code="BEAM_CENTER_OFFSET",
                    message=(
                        f"Estimated beam centre ({cy:.1f}, {cx:.1f}) is {offset:.1f} px "
                        f"from the geometric centre ({rc_y:.1f}, {rc_x:.1f}). "
                        "Radial fingerprints and diffraction-class labels may be biased."
                    ),
                    evidence={
                        "estimated_center_yx": [round(cy, 3), round(cx, 3)],
                        "radial_center_yx": [round(rc_y, 3), round(rc_x, 3)],
                        "offset_pixels": round(offset, 3),
                    },
                )
            )
    except Exception:
        pass  # best-effort; do not crash the QC pass


def _check_saturation(virtual: VirtualImageResult | None, flags: list[QCFlag]) -> None:
    if virtual is None:
        return
    try:
        max_dp = np.asarray(virtual.max_diffraction, dtype=np.float32)
        saturated = max_dp >= np.percentile(max_dp, 99.9)
        sat_fraction = float(np.mean(saturated))
        if sat_fraction > 0.05:
            flags.append(
                QCFlag(
                    severity="warning",
                    code="SATURATION_HIGH",
                    message=(
                        f"{sat_fraction:.1%} of diffraction patterns show saturation "
                        f"in the 99.9th percentile of the max-diffraction image. "
                        "COM-x / COM-y and radial profiles may be distorted."
                    ),
                    evidence={"saturation_fraction": round(sat_fraction, 5)},
                )
            )
    except Exception:
        pass


def _check_orientation_confidence(
    orientation: OrientationResult | None,
    confidence_threshold: float,
    flags: list[QCFlag],
) -> None:
    if orientation is None:
        return
    try:
        score = np.asarray(orientation.score, dtype=np.float32)
        unreliable = score < confidence_threshold
        unreliable_frac = float(np.mean(unreliable))
        if unreliable_frac > 0.5:
            flags.append(
                QCFlag(
                    severity="warning",
                    code="LOW_ORIENTATION_CONFIDENCE",
                    message=(
                        f"{unreliable_frac:.1%} of orientation-preview positions have "
                        f"scores below the confidence threshold ({confidence_threshold}). "
                        "Orientation preview may be unreliable; consider using templates."
                    ),
                    evidence={
                        "unreliable_fraction": round(unreliable_frac, 5),
                        "confidence_threshold": confidence_threshold,
                    },
                )
            )
    except Exception:
        pass


def _check_roi_candidates(diagnostics: dict[str, Any] | None, flags: list[QCFlag]) -> None:
    if diagnostics is None:
        flags.append(
            QCFlag(
                severity="info",
                code="ROI_CANDIDATES_NOT_GENERATED",
                message="ROI candidates were not generated (diagnostics disabled or failed).",
            )
        )
        return
    roi_outputs = diagnostics.get("roi_outputs", {})
    if not roi_outputs:
        flags.append(
            QCFlag(
                severity="info",
                code="ROI_CANDIDATES_EMPTY",
                message="No ROI candidates were generated. Consider inspecting the cluster and boundary maps manually.",
            )
        )
        return
    # Try to count ROIs from the YAML path.
    yaml_path = roi_outputs.get("yaml")
    if yaml_path and Path(str(yaml_path)).exists():
        try:
            import yaml

            data = yaml.safe_load(Path(str(yaml_path)).read_text(encoding="utf-8"))
            rois = data.get("rois", []) if isinstance(data, dict) else []
            n_rois = len(rois)
            if n_rois == 0:
                flags.append(
                    QCFlag(
                        severity="info",
                        code="ROI_CANDIDATES_EMPTY",
                        message="ROI candidates YAML exists but contains no ROIs.",
                    )
                )
            elif n_rois < 3:
                flags.append(
                    QCFlag(
                        severity="info",
                        code="ROI_CANDIDATES_FEW",
                        message=f"Only {n_rois} ROI candidate(s) found. More ROIs may help Stage-2 coverage.",
                        evidence={"n_rois": n_rois},
                    )
                )
        except Exception:
            pass


def _check_sample_mask(
    sample_mask: np.ndarray | None,
    phase: PhaseScreeningResult | None,
    flags: list[QCFlag],
) -> None:
    if sample_mask is None:
        return
    try:
        sample_frac = float(sample_mask.mean())
        if sample_frac < 0.1:
            flags.append(
                QCFlag(
                    severity="warning",
                    code="SAMPLE_FRACTION_LOW",
                    message=(
                        f"Sample mask covers only {sample_frac:.1%} of the field of view. "
                        "Most of the scan is background / vacuum / excluded."
                    ),
                    evidence={"sample_fraction": round(sample_frac, 5)},
                )
            )

        # Check for background pixels accidentally labeled as sample
        if phase is not None:
            labels = np.asarray(phase.labels)
            n_background = int(np.sum(labels == -1))
            if n_background > 0:
                flags.append(
                    QCFlag(
                        severity="info",
                        code="SAMPLE_MASK_APPLIED",
                        message=(
                            f"Sample mask applied: {int(sample_mask.sum())} sample pixels, "
                            f"{n_background} background pixels (label -1)."
                        ),
                        evidence={
                            "sample_pixels": int(sample_mask.sum()),
                            "background_pixels": n_background,
                            "sample_fraction": round(sample_frac, 5),
                        },
                    )
                )
    except Exception:
        pass


