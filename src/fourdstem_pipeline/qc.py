"""Stage-1 quality-control checks.

Each check inspects one aspect of the pipeline output and produces a
:class:`QCFlag` when thresholds are exceeded.  The aggregated result is
a ``qc_summary.json`` / ``qc_summary.md`` that tells the user right
away whether this Stage-1 run is trustworthy and which parts need
careful interpretation.
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

import numpy as np

from .dataset import DatasetHandle
from .fingerprints import FingerprintResult
from .orientation import OrientationResult
from .phase import PhaseScreeningResult
from .virtual import VirtualImageResult


# ---------------------------------------------------------------------------
# Data structures
# ---------------------------------------------------------------------------


@dataclass
class QCFlag:
    """A single quality-control finding."""

    severity: str  # "info", "warning", "critical"
    code: str
    message: str
    evidence: dict[str, Any] = field(default_factory=dict)


@dataclass
class QCResult:
    """Aggregated QC outcome for a Stage-1 run."""

    stage1_status: str  # "PASS" | "PASS_WITH_WARNINGS" | "FAIL"
    n_warnings: int
    n_critical: int
    flags: list[QCFlag] = field(default_factory=list)

    def to_dict(self) -> dict[str, Any]:
        return {
            "stage1_status": self.stage1_status,
            "n_warnings": self.n_warnings,
            "n_critical": self.n_critical,
            "flags": [
                {
                    "severity": flag.severity,
                    "code": flag.code,
                    "message": flag.message,
                    "evidence": flag.evidence,
                }
                for flag in self.flags
            ],
        }


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def run_qc_checks(
    dataset: DatasetHandle | None,
    virtual: VirtualImageResult | None,
    fingerprints: FingerprintResult | None,
    phase: PhaseScreeningResult | None,
    orientation: OrientationResult | None,
    diagnostics: dict[str, Any] | None,
    errors: list[dict[str, Any]] | None,
    *,
    confidence_threshold: float = 0.05,
    sample_mask: np.ndarray | None = None,
) -> QCResult:
    """Run all Stage-1 QC checks and return an aggregated result.

    Parameters
    ----------
    dataset:
        The preprocessed dataset handle (may be ``None`` if loading failed).
    virtual:
        Virtual-image results from Stage 1.
    fingerprints:
        Radial-fingerprint results from Stage 2.
    phase:
        Phase-screening results from Stage 3.
    orientation:
        Orientation-preview results from Stage 4.
    diagnostics:
        Dict of diagnostic output paths / metadata.
    errors:
        List of stage-level errors captured during the run.
    confidence_threshold:
        Threshold used for orientation reliability (same as the workflow
        ``confidence_threshold`` parameter).
    """
    flags: list[QCFlag] = []

    # --- Always-present informational flags --------------------------------
    flags.append(
        QCFlag(
            severity="info",
            code="UNSUPERVISED_LABELS_ONLY",
            message=(
                "Fingerprint-class labels are generated by unsupervised "
                "clustering of radial fingerprints. They are NOT confirmed "
                "crystallographic phases and require Stage-2 validation "
                "(py4DSTEM / pyxem Bragg indexing or template matching)."
            ),
        )
    )

    # --- Upstream errors ---------------------------------------------------
    if errors:
        for err in errors:
            flags.append(
                QCFlag(
                    severity="critical",
                    code="STAGE_ERROR",
                    message=f"Stage '{err.get('stage', 'unknown')}' failed: {err.get('error', 'unknown error')}",
                    evidence={"stage": err.get("stage"), "elapsed_s": err.get("elapsed_s")},
                )
            )

    # --- 1. Input shape ----------------------------------------------------
    _check_input_shape(dataset, flags)

    # --- 2. Virtual images -------------------------------------------------
    _check_virtual_images(virtual, flags)

    # --- 3. Radial fingerprints -------------------------------------------
    _check_radial_fingerprints(fingerprints, flags)

    # --- 4 / 5. Cluster labels + sizes -------------------------------------
    _check_cluster_labels(phase, flags)
    _check_cluster_sizes(phase, flags)

    # --- 6. Beam centre offset ---------------------------------------------
    _check_beam_center(virtual, flags)

    # --- 7. Saturation fraction --------------------------------------------
    _check_saturation(virtual, flags)

    # --- 8. Orientation confidence -----------------------------------------
    _check_orientation_confidence(orientation, confidence_threshold, flags)

    # --- 9. ROI candidates -------------------------------------------------
    _check_roi_candidates(diagnostics, flags)

    # --- 10. Sample mask ----------------------------------------------------
    _check_sample_mask(sample_mask, phase, flags)

    # --- Aggregate ----------------------------------------------------------
    n_critical = sum(1 for f in flags if f.severity == "critical")
    n_warnings = sum(1 for f in flags if f.severity == "warning")

    if n_critical > 0:
        status = "FAIL"
    elif n_warnings > 0:
        status = "PASS_WITH_WARNINGS"
    else:
        status = "PASS"

    return QCResult(
        stage1_status=status,
        n_warnings=n_warnings,
        n_critical=n_critical,
        flags=flags,
    )


def save_qc_summary(output_dir: str | Path, qc_result: QCResult) -> tuple[Path, Path]:
    """Write ``qc_summary.json`` and ``qc_summary.md`` to *output_dir*."""
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    json_path = out / "qc_summary.json"
    json_path.write_text(
        json.dumps(_jsonable(qc_result.to_dict()), indent=2), encoding="utf-8"
    )

    md_path = out / "qc_summary.md"
    md_path.write_text(_render_qc_markdown(qc_result), encoding="utf-8")

    return json_path, md_path


